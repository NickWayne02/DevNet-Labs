# file-access-input.py

# Відкриття файлу в режимі додавання
with open('devices.txt', 'a') as file:
    while True:
        # Запит нового пристрою у користувача
        новий_пристрій = input("Введіть назву пристрою: ")
        # Преривання циклу при введенні 'exit'
        if новий_пристрій.lower() == 'exit':
            print("Усі завдання виконано!")
            break
        # Запис нового пристрою в файл
        file.write(новий_пристрій + "\n")
